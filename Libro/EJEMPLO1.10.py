BASE = float ( input (" Escribe la base del rectangulo: " ))
ALTURA = float ( input (" Escribe la altura del rectangulo: " ))
SUPERFICIE = BASE * ALTURA
PERIMETRO = 2*(BASE + ALTURA)
print(f"Un cuadrado de base {BASE} y altura {ALTURA} su superficie es {SUPERFICIE}")
print(f"Un cuadrado de base {BASE} y altura {ALTURA} su perimetro es {PERIMETRO}")
